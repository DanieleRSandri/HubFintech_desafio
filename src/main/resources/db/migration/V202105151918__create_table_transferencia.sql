CREATE TABLE TRANSFERENCIA(
	TRANSFERENCIA_ID INTEGER NOT NULL,
	CONTA_ID INTEGER NOT NULL,
	VALOR VARCHAR2(250) NOT NULL,	
	DESCRICAO VARCHAR2(250) NOT NULL,
	FOREIGN KEY(CONTA_ID) REFERENCES CONTA (CONTA_ID),
    PRIMARY KEY (TRANSFERENCIA_ID)
);

CREATE SEQUENCE TRANSFERENCIA_ID_SEQ START WITH 1;
